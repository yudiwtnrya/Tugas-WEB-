     */
    public function store(Request $request)
    {
      $data = $request->validate([
          'nama_orangtua'=>['required'],
          'nik_ortu'=>['required'],
          'alamat'=>['required'],
          'ibu_atau_ayah'=>['required'],
          'tgl_lahir'=>['required'],
          'jenis_kelamin'=>['required'],
      ]);
      $data['id_admin']=1;
      $data['no_region']=1;
      Orangtua::create($data);
      return redirect('/orangtua');
    }

